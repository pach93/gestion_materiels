        if(!JSType.isNumber(prTFD.getText()))
        {
        JOptionPane.showMessageDialog(null, "Le prix doit etre num√©rique", "Gestion Employe", JOptionPane.INFORMATION_MESSAGE);
        prTFD.setText("");
        return;
        }
        if(prTFD.equals("")||nomTFD.equals(""))
        {
        JOptionPane.showMessageDialog(null, "Veuillez saisir tous les champs", "Gestion Employe", JOptionPane.INFORMATION_MESSAGE);
        return;
        }
     Materiel mat = new Materiel();
     
     mat.setNom(nomTFD.getText());
     mat.setPrix(Float.parseFloat(prTFD.getText()));
             
        try {
            int i = MatosBean.addMatos(mat);
            if(i!=0){
                JOptionPane.showMessageDialog(null, "Bon", "Gestion Employe", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erreur" + ex.getMessage(), "Gestion Employe", JOptionPane.INFORMATION_MESSAGE);
        }
